"""
Tipo numérico

Tipo Int - Inteiro

Tipo Float - Real, decimal - Separador por ponto, e não vírgula
"""

# int

i = 3
j = 2
print(f'i = {i}')
print(f'i = {j}')
print(f'tipo de i', type(i))
k = i + j
print(f'i+j ={k}')
k = i - j
print(f'i-j ={k}')
k = i * j
print(f'i*j ={k}')
k = i ** j
print(f'i**j ={k}')
k = i / j
print(f'i/j ={k}')
k = i // j
print(f'i//j ={k}')
k = i % j
print(f'i%j ={k}')

# float

v = 4.33
print(v)
print(type(v))
# dupla atribuição
v1, v2 = 3.2, 5.8
print(f'v1 ={v1}, v2 = {v2}')
print('As operações são quase as mesmas para int')
print('para variável complexa adiciona-se j, exemplo 5j')

